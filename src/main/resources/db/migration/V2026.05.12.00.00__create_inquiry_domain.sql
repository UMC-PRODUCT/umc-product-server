    ADD CONSTRAINT FK_INQUIRY_MESSAGE_ON_INQUIRY FOREIGN KEY (inquiry_id) REFERENCES inquiry (id);

ALTER TABLE inquiry_attachment
    ADD CONSTRAINT FK_INQUIRY_ATTACHMENT_ON_INQUIRY FOREIGN KEY (inquiry_id) REFERENCES inquiry (id);

ALTER TABLE inquiry_attachment
    ADD CONSTRAINT FK_INQUIRY_ATTACHMENT_ON_FILE_METADATA FOREIGN KEY (file_metadata_id) REFERENCES file_metadata (id);

ALTER TABLE inquiry_message_attachment
    ADD CONSTRAINT FK_INQUIRY_MESSAGE_ATTACHMENT_ON_INQUIRY_MESSAGE FOREIGN KEY (inquiry_message_id) REFERENCES inquiry_message (id);

ALTER TABLE inquiry_message_attachment
    ADD CONSTRAINT FK_INQUIRY_MESSAGE_ATTACHMENT_ON_FILE_METADATA FOREIGN KEY (file_metadata_id) REFERENCES file_metadata (id);

