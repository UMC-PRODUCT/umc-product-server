CREATE TABLE inquiry
(
    id                   BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    created_at           TIMESTAMP WITH TIME ZONE                NOT NULL,
    updated_at           TIMESTAMP WITH TIME ZONE                NOT NULL,
    title                VARCHAR(255)                            NOT NULL,
    content              TEXT                                    NOT NULL,
    category             VARCHAR(30)                             NOT NULL,
    target               VARCHAR(20)                             NOT NULL,
    status               VARCHAR(20)                             NOT NULL DEFAULT 'RECEIVED',
    author_challenger_id BIGINT                                  NOT NULL,
    is_read              BOOLEAN                                 NOT NULL DEFAULT FALSE,
    CONSTRAINT pk_inquiry PRIMARY KEY (id)
);

CREATE TABLE inquiry_message
(
    id                   BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    created_at           TIMESTAMP WITH TIME ZONE                NOT NULL,
    updated_at           TIMESTAMP WITH TIME ZONE                NOT NULL,
    inquiry_id           BIGINT                                  NOT NULL,
    sender_type          VARCHAR(20)                             NOT NULL,
    sender_challenger_id BIGINT                                  NOT NULL,
    content              TEXT                                    NOT NULL,
    CONSTRAINT pk_inquiry_message PRIMARY KEY (id)
);

CREATE TABLE inquiry_attachment
(
    id               BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    created_at       TIMESTAMP WITH TIME ZONE                NOT NULL,
    updated_at       TIMESTAMP WITH TIME ZONE                NOT NULL,
    inquiry_id       BIGINT                                  NOT NULL,
    file_metadata_id VARCHAR(255)                            NOT NULL,
    CONSTRAINT pk_inquiry_attachment PRIMARY KEY (id)
);

CREATE TABLE inquiry_message_attachment
(
    id                 BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    created_at         TIMESTAMP WITH TIME ZONE                NOT NULL,
    updated_at         TIMESTAMP WITH TIME ZONE                NOT NULL,
    inquiry_message_id BIGINT                                  NOT NULL,
    file_metadata_id   VARCHAR(255)                            NOT NULL,
    CONSTRAINT pk_inquiry_message_attachment PRIMARY KEY (id)
);

